# 字符串
str1 = 'Python Hello World1'
str2 = "Python Hello World2"
print("str1: ",str1)
print("str2: ",str2)
# 字符串截取
print("str1[0]: ",str1[0])
print("str2[1:5]: ",str2[1:5])
# 字符串拼接
print("str1[0]: ",str1[0] + str2[1:5])
# 字符重复输出100遍
print("str1 * 100",str1 * 100)
# in 包含某字符
print("\'H\' in str1:",'H' in str1)
# not in 不包含某字符
print("\'H1\' not in str1:",'H1' in str1)
# %s 字符串格式化 %d 数字格式化
print("jdsifsdf %s and whehi is %d"%('222',2))